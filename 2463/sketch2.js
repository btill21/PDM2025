function setup() {
  
    //size of our canvas x,y
    createCanvas(720,400);
    
    // bg color 255 being white
    background(255);
    
    // no stroke to remove outline
    noStroke();
    
    //r,b,g, hue/transp
    fill(255, 0, 0, 67);  
    
    //circle x,y,z
    circle(200, 111, 150);
    
    fill(0, 255, 0, 67);  
    
    //circle x,y,z
    circle(250, 200, 150);
    
    fill(0, 0, 255, 67);  
    //circle x,y,z
    circle(150, 200, 150);
  }
  